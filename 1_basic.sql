SELECT - ekstrak data dari database
UPDATE - update data dalam database
DELETE - menghapus data dari database
INSERT INTO - memasukkan data baru ke dalam database
CREATE DATABASE - membuat database baru
ALTER DATABASE -memodifikasi database
CREATE TABLE - membuat tabel baru
ALTER TABLE - memodifikasi sebuah tabel
DROP TABLE - menghapus sebuah tabel
CREATE INDEX - membuat indeks (tombol pencarian)
DROP INDEX - menghapus indeks
